    public void ingresarCabeceraTabla() {
        String[] cabecera = new String[]{"Nombre", "Apellido", "Telefono", "Correo"};
        contactos.setColumnIdentifiers(cabecera);
        TblContactos.setModel(contactos);
    }

    public void limpiarTabla() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) TblContactos.getModel();
            int filas = TblContactos.getRowCount();
            for (int i = 0; i <= filas; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception e) {
            // JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
    }

    public void leerTabla() {
        String nombreArchivo;
        nombreArchivo = TxtUbicacion.getText();
        TblContactos.getSelectedRow();
        for (int i = 0; i < TblContactos.getRowCount(); i++) {
            if (i == TblContactos.getSelectedRow()) {
            } else {
                RegItems nuevo = new RegItems();
                nuevo.setNombre(TblContactos.getValueAt(i, 0).toString());
                nuevo.setApellido(TblContactos.getValueAt(i, 1).toString());
                nuevo.setTelefono(TblContactos.getValueAt(i, 2).toString());
                nuevo.setCorreo(TblContactos.getValueAt(i, 3).toString());
                file.agregarContacto(nuevo, nombreArchivo);
            }
        }
    }

    private void BtnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSeleccionarActionPerformed
        String nombreArchivo;
        limpiarTabla();
        nombreArchivo = TxtUbicacion.getText();
        file.tablelistContactos(contactos, nombreArchivo);
    }//GEN-LAST:event_BtnSeleccionarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        String nombreArchivo;
        nombreArchivo = TxtUbicacion.getText();
        file.eliminarArchivo(nombreArchivo);
        file.crearArchivo(nombreArchivo);
        leerTabla();
        JOptionPane.showMessageDialog(null, "Contacto Eliminado Satisfactoriamente");
        TxtUbicacion.setText("");
        limpiarTabla();
    }//GEN-LAST:event_BtnEliminarActionPerformed

